package com.medigy.persist.util;

public class DelimitedValuesReaderException extends RuntimeException
{
    public DelimitedValuesReaderException()
    {
    }

    public DelimitedValuesReaderException(Throwable cause)
    {
        super(cause);
    }

    public DelimitedValuesReaderException(String message)
    {
        super(message);
    }

    public DelimitedValuesReaderException(String message, Throwable cause)
    {
        super(message, cause);
    }
}
